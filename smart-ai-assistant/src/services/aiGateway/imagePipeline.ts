//  IMAGE PIPELINE  —  Cloudflare → HuggingFace → Gemini text
//  FIXES:
//  ✅ Cloudflare first (most reliable for actual images)
//  ✅ NEVER shows false "safety policy" for no-image response
//  ✅ imageUrl:null returns mode:'described', not an error
//  ✅ Proper success/failure distinction
